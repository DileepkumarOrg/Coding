public class Operators {
    public static void main(String args[])
    {
        int x=10;
        System.out.println(x++);
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);
        System.out.println(x--);
        System.out.println(x);
        System.out.println(--x);
        System.out.println(x);
        int a=10;
        int b=5;
        //boolean c=a < b;
        System.out.println(a<b);
    }
}